
public class Incearca {

	private int age;
	private String nume;
	
	public Incearca(String nume) {
		this. setNume(nume);
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getNume() {
		return nume;
	}

	public void setNume(String nume) {
		this.nume = nume;
	}
}
